    private void bt_registrar_maestroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_registrar_maestroActionPerformed
        // TODO add your handling code here:
         String nombre;
        String user;
        String Password;
        String confirm;
        int rrhh;
        
        try{
            nombre = tf_nombre.getText();
            user = tf_usuario.getText();
            Password = tf_password.getText();
            confirm = pf_password.getText();
            rrhh = Integer.parseInt(tf_recursos.getText());
            if (!(confirm.equals(Password))){
                JOptionPane.showMessageDialog(null, "Las contrase√±as no coinciden");
            }else{
                Maestro x = new Maestro (rrhh, nombre, user, Password);
                Maestro.add(x);
                
                dba db = new dba("./neoBlackboard1.accdb");
                db.conectar();
                
                db.query.execute("INSERT INTO Maestros"
                    + " (Nombre,RRHH)"
                    + " VALUES ('" + nombre + "', '" + rrhh + "')");
                db.commit();
                
                JOptionPane.showMessageDialog(null, "Se ha registrado un nuevo maestro ");
                db.desconectar();
                
                tf_nombre.setText("");
                tf_usuario.setText("");
                tf_password.setText("");
                pf_password.setText("");
                
               
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Ha ocurrido un error");
        }
    }//GEN-LAST:event_bt_registrar_maestroActionPerformed

